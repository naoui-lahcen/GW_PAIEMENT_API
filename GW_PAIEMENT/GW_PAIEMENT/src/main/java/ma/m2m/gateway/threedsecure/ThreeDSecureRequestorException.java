package ma.m2m.gateway.threedsecure;

public class ThreeDSecureRequestorException extends Exception {

	private static final long serialVersionUID = 1L;

	public ThreeDSecureRequestorException() {
		super();
	}

	public ThreeDSecureRequestorException(String message) {
		super(message);
	}

	public ThreeDSecureRequestorException(Throwable cause) {
		super(cause);
	}
	
	public ThreeDSecureRequestorException(String message, Throwable cause) {
		super(message, cause);
	}
	


}
